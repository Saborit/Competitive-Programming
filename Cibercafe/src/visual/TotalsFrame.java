    public TotalsFrame(Cibercafe cibercafe) {
        initComponents();
        
        
        String[] columnNames = {"Usuario", "Servicio Solicitado", "Monto a Pagar"}; 
        
        int size = 0;
        for(User user: cibercafe.getAllCostumers())
            size += user.getServices().size();
        
        String[][] data = new String[size][3];
        
        int row = 0;
        for(User user: cibercafe.getAllCostumers()){
            for(Service serv: user.getServices()){
                data[row][0] = user.getName();
                data[row][2] = String.format("%.2f", serv.calcPrice(user));
                
                if( serv instanceof BurnService )
                    data[row][1] = "Quemado";
                else if( serv instanceof BrowseService )
                    data[row][1] = "Navegaci贸n";
                else if( serv instanceof PrintService )
                    data[row][1] = "Impresi贸n";
             
                row++;
            }
        }
        
        userServiceTable.setModel(new DefaultTableModel(data, columnNames));
        
        columnNames = new String[]{"Usuario", "Monto a Pagar"}; 
        
        data = new String[cibercafe.getAllCostumers().size()][3];
        
        User user;
        double total;
        for(int i=0; i < cibercafe.getAllCostumers().size(); i++ ){
            user = cibercafe.getAllCostumers().get(i);
            total = 0;
            
            for(Service serv: user.getServices())
                total += serv.calcPrice(user);
            
            data[i][0] = user.getName();
            data[i][1] = String.format("%.2f", total);
        }
        
        usersTable.setModel(new DefaultTableModel(data, columnNames));
        
        
        columnNames = new String[]{"Servicio", "Ganancia Total"}; 
        data = new String[3][2];
        
        double totBrowse = 0.0, totPrint = 0.0, totBurn = 0.0;
        for(User _user: cibercafe.getAllCostumers()){
            for(Service serv: _user.getServices()){
                if( serv instanceof BrowseService )
                    totBrowse += serv.calcPrice(_user);
                else if( serv instanceof PrintService )
                    totPrint += serv.calcPrice(_user);
                else if( serv instanceof BurnService )
                    totBurn += serv.calcPrice(_user);
            }
        }
        
        data[0][0] = "Navegaci贸n";
        data[1][0] = "Impresi贸n";
        data[2][0] = "Quemado";
        
        data[0][1] = String.format("%.2f", totBrowse);
        data[1][1] = String.format("%.2f", totPrint);
        data[2][1] = String.format("%.2f", totBurn);
        
        servicesTable.setModel(new DefaultTableModel(data, columnNames));
        
        userServiceTable.setEnabled(false);
        usersTable.setEnabled(false);
        servicesTable.setEnabled(false);
    }
