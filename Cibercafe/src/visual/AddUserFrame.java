    private void finalizeButtonAddUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_finalizeButtonAddUserActionPerformed
        User user = null;
        
        try{
            if( getCheckBoxAddUser().isSelected() )
                user = caller.main.registerAsociatedUserFromAddUserFrame(this);
            else
                user = new EventualUser(getNameTextField().getText(), getCardTextField().getText(), -1);
        
            caller.main.enQueueUser(user);
            dispose();
        }
        catch(ExistingUserException eue){
            caller.main.launchErrorDialog(this, eue);
        }
    }//GEN-LAST:event_finalizeButtonAddUserActionPerformed

