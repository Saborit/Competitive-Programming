    private boolean canEnableFinalize(){
        boolean flag = true;
        
        flag &= Utils.areAllDigits(getCardTextField().getText());
        flag &= !nameTextField.getText().equals("");
        
        if( getCheckBoxAddUser().isSelected() ){
            flag &= Utils.isNonNegativeInteger(getYearsTextField().getText());
            flag &= !listCategories.isSelectionEmpty();
        }
        
        return flag;
    } 

