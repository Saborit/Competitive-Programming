    void setList(Object[] list){
        users = new ArrayList<>();
        users.addAll(Arrays.asList(list));
        usersList.setListData(list);
    }
    
    void setList(List list){
        Object[] newList = new Object[list.size()];
        for(int i=0; i < list.size(); i++)
            newList[i] = list.get(i);
        setList(newList);
    }

